#include <stdio.h>
/*
 *  计算存款利息，三种方案
 *      1：活期，年利率0.0036
 *      2:一年期定期，年利率0.0225
 *      3:存两次半年定期，年利率0.0198
 */
int main(){
    float p0 = 1000;    //定义本金为p0
    float r1 = 0.0036,r2 = 0.0225,r3 = 0.0198;  //定义三中方法的利率，分别为r1,r2,r3
    float p1,p2,p3; //定义计算后的总金

    p1 = p0 * (1 + r1);
    p2 = p0 * (1 + r2);
    p3 = p0 * (1 + r3/2) * (1+ r3/2);

    printf(" p1 = %f\n p2 = %f\n p3 = %f\n",p1,p2,p3);

    return 0;
}